package com.trancendos.alervato.model;

/**
 * Represents the status of a cost item.
 */
public enum CostStatus {
    /**
     * The cost item is pending approval.
     */
    PENDING,
    /**
     * The cost item has been approved.
     */
    APPROVED,
    /**
     * The cost item has been rejected.
     */
    REJECTED
}
